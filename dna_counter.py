# dna_counter.py

# Sample DNA sequence
dna_sequence = "AGCTTAGCTAAGGCT"

# Count each base
a_count = dna_sequence.count("A")
t_count = dna_sequence.count("T")
c_count = dna_sequence.count("C")
g_count = dna_sequence.count("G")

# Print results
print("DNA Base Counts:")
print("A:", a_count)
print("T:", t_count)
print("C:", c_count)
print("G:", g_count)

